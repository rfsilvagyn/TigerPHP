////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
$('#btnCancelar').on('click', function(){

  var ids = [];
  var titulos = document.querySelectorAll('.marcaTitulo:checked');

  for(var i = 0; i < titulos.length; i++){
    ids.push(titulos[i].value);
  }

  $('#idsCancelar').val(ids);
  $('#dadosCancelar').submit();

})
