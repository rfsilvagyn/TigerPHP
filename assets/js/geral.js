$(document).ready(function() {
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //CONFIGURACAO FORMATO DA DATA PARA ORDENAR O DATATABLE
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  $.fn.dataTable.moment('DD/MM/YYYY');
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //DESATIVA TECLA ENTER
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  $('input').keypress(function (e) {
    var code = null;
    code = (e.keyCode ? e.keyCode : e.which);
    return (code == 13) ? false : true;
  });
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //CONFIGURACAO MASCARA DOS CAMPOS
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  $('.data').inputmask('dd/mm/yyyy');
  $('.hora').inputmask('99:99');
  $(".valor").inputmask('decimal', {
    'alias': 'numeric',
    'groupSeparator': '.',
    'autoGroup': true,
    'digits': 2,
    'radixPoint': ",",
    'digitsOptional': false,
    'allowMinus': false,
    'placeholder': ''
  });
  $('.quantidade').inputmask({
    alias: 'numeric',
    allowMinus: false,
    digits: 0,
    max: 100
  });
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

});
