////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//VERIFICA TITULOS SELECIONADOS E CHAMA IMPRESSAO DO CARNE
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
$('#btnImprimirCarne').on('click', function(){

  var ids = [];
  var titulos = document.querySelectorAll('.marcaTitulo:checked');

  for(var i = 0; i < titulos.length; i++){
    ids.push(titulos[i].value);
  }

  $('#ids').val(ids);
  $('#dados').submit();

})
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//MARCA / DESMARCA TODOS OS TITULOS
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
$('#marcaTodos').on('click', function(){
  $('.marcaTitulo').each(
    function() {
      if ($(this).prop("checked")) {
        $(this).prop("checked", false);
      } else {
        $(this).prop("checked", true);
      }
    })

  })
